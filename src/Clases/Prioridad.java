package Clases;

/**
 * Enumeración que establece los niveles de prioridad para las tareas.
 * Ayuda a clasificar y gestionar la importancia de cada tarea.
 * 
 * @author [Aaron Marek]
 * @version 1.0
 */
public enum Prioridad {
    BAJA,      // Tarea de menor urgencia
    MEDIA,     // Tarea con importancia moderada
    ALTA,      // Tarea importante que requiere atención pronto
    URGENTE    // Tarea crítica que necesita atención inmediata
}